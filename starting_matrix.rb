def generate_matrix string
  puzzle = []
  row = []
  string.each_char do |char|
    if char == "0"
      row << [1,2,3,4,5,6,7,8,9]
    else
      row << char.to_i
    end

    if row.length == 9
      puzzle << row
      row = []
    end
  end
  puzzle
end

def starting_matrix sudoku
  generate_matrix(sudoku)
end


